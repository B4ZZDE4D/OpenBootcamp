public class NumIf {
    public static void main(String[] args) throws Exception {

        int numeroIf = -2;

        if (numeroIf > 0) {
            System.out.println("El numero " + numeroIf + " es positivo");

        } else if (numeroIf == 0) {
            System.out.println("El numero " + numeroIf + " es cero");
        } else {
            System.out.println("El numero " + numeroIf + " es negativo");
        }

    }
}
